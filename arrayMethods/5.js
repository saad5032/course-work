// adding things to array
var fruits = ["apple","banana","lemon","orange"]
var morefruits = fruits.concat("kiwi","watermelon","pineapple")
console.log(morefruits);

// joing the strings
var joinedfruits = fruits.join("-")    // convert array to strings
console.log(joinedfruits);

// reversing the array
var reversedArray = fruits.reverse()
console.log(reversedArray);

// to know index of element in array
var index = fruits.indexOf("orange")   // since I reversed the array orange 
console.log(index);







